<?php

namespace App\Services;

use App\Models\Sale;

class InvoiceService
{
    public function generateInvoiceData(
        Sale $sale
    ): array {

        return [

            /*
            |--------------------------------------------------------------------------
            | RECEIPT
            |--------------------------------------------------------------------------
            */

            'receipt_no' =>

                $sale->receipt_no,

            /*
            |--------------------------------------------------------------------------
            | BRANCH
            |--------------------------------------------------------------------------
            */

            'branch' =>

                $sale->user?->branch?->name
                ?? 'Main Branch',

            'branch_address' =>

                $sale->user?->branch?->address
                ?? 'Kigali - Rwanda',

            'branch_phone' =>

                $sale->user?->branch?->phone
                ?? '0780000000',

            /*
            |--------------------------------------------------------------------------
            | CASHIER
            |--------------------------------------------------------------------------
            */

            'cashier' =>

                $sale->user?->name
                ?? 'Cashier',

            /*
            |--------------------------------------------------------------------------
            | SHIFT
            |--------------------------------------------------------------------------
            */

            'shift_id' =>

                $sale->shift_id,

            /*
            |--------------------------------------------------------------------------
            | DATE
            |--------------------------------------------------------------------------
            */

            'date' =>

                $sale->created_at
                    ?->format('Y-m-d H:i'),

            /*
            |--------------------------------------------------------------------------
            | ITEMS
            |--------------------------------------------------------------------------
            */

            'items' =>

                $sale->items,

            /*
            |--------------------------------------------------------------------------
            | FINANCIALS
            |--------------------------------------------------------------------------
            */

            'subtotal' =>

                $sale->subtotal,

            'tax' =>

                $sale->tax
                ?? 0,

            'discount' =>

                $sale->discount
                ?? 0,

            'grand_total' =>

                $sale->grand_total,

            /*
            |--------------------------------------------------------------------------
            | PAYMENT
            |--------------------------------------------------------------------------
            */

            'payment_method' =>

                $sale->payment_method,

            'amount_paid' =>

                $sale->amount_paid,

            'change_amount' =>

                $sale->change_amount,

            /*
            |--------------------------------------------------------------------------
            | STATUS
            |--------------------------------------------------------------------------
            */

            'payment_status' =>

                $sale->payment_status,

            'sale_status' =>

                $sale->sale_status,

            /*
            |--------------------------------------------------------------------------
            | CURRENCY
            |--------------------------------------------------------------------------
            */

            'currency' =>

                'RWF',

        ];
    }
}