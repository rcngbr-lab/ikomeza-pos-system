<?php

namespace App\Services;

use App\Models\AuditLog;

class AuditService
{
    public static function log(

        string $event,

        ?string $model = null,

        ?string $description = null,

        ?int $modelId = null,

        ?array $oldValues = null,

        ?array $newValues = null,

        ?string $severity = 'INFO'

    ): void {

        $user = auth()->user();

        AuditLog::create([

            /*
            |--------------------------------------------------------------------------
            | USER
            |--------------------------------------------------------------------------
            */

            'user_id' =>

                $user?->id,

            /*
            |--------------------------------------------------------------------------
            | BRANCH
            |--------------------------------------------------------------------------
            */

            'branch_id' =>

                $user?->branch_id,

            /*
            |--------------------------------------------------------------------------
            | EVENT
            |--------------------------------------------------------------------------
            */

            'event' =>

                strtoupper($event),

            /*
            |--------------------------------------------------------------------------
            | MODEL
            |--------------------------------------------------------------------------
            */

            'model' =>

                $model,

            'model_id' =>

                $modelId,

            /*
            |--------------------------------------------------------------------------
            | DESCRIPTION
            |--------------------------------------------------------------------------
            */

            'description' =>

                $description,

            /*
            |--------------------------------------------------------------------------
            | DATA SNAPSHOTS
            |--------------------------------------------------------------------------
            */

            'old_values' =>

                $oldValues
                    ? json_encode($oldValues)
                    : null,

            'new_values' =>

                $newValues
                    ? json_encode($newValues)
                    : null,

            /*
            |--------------------------------------------------------------------------
            | SEVERITY
            |--------------------------------------------------------------------------
            */

            'severity' =>

                strtoupper($severity),

            /*
            |--------------------------------------------------------------------------
            | REQUEST
            |--------------------------------------------------------------------------
            */

            'ip_address' =>

                request()->ip(),

            'user_agent' =>

                request()->userAgent(),

        ]);
    }
}
